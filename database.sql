  SELECT A.QTY, A.SUBTOTAL, A.ID_PESAN, B.NAMA_PELANGGAN, C.NAMA_BARANG, C.HARGA_BARANG
FROM DETAIL A JOIN LIST_PEMESANAN B
ON A.ID_PESAN = B.ID_PESAN
JOIN BARANG C
ON A.ID_BARANG = C.ID_BARANG
;
